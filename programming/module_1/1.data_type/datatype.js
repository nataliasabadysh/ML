// Number: Used for both integers and floating-point numbers
let age = 30;
let averageScore = 85.6;
// String: Used for text
let personName = "John Doe";
// Boolean: Used for true/false values
let isStudent = true;
// Undefined: Used for variables that have not been assigned a value
let address;

// Null: Used to represent the intentional absence of a value
let data = null;

// Array: Used for ordered collections of items
let fruits = ["apple", "banana", "cherry"];
// Object: Used for key-value pairs
let studentInfo = { name: "Alice", grade: "A", age: 22 };
// Symbol: Used for unique identifiers
let uniqueId = Symbol("id");
// BigInt: Used for integers larger than the Number type can hold
let largeNumber = BigInt("9007199254740991");

// Print the variables
console.log("Age:", age);
console.log("Average Score:", averageScore);
console.log("Name:", personName);
console.log("Is Student:", isStudent);
console.log("Address:", address);
console.log("Data:", data);
console.log("Fruits:", fruits);
console.log("Student Info:", studentInfo);
console.log("Unique ID:", uniqueId);
console.log("Large Number:", largeNumber);
