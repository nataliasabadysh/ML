# 1. Integer: Used for whole numbers
age = 30
# 2. Float: Used for decimal numbers
average_score = 85.6

# Creating complex numbers
a = 2 + 3j  # 2 is the real part, and 3j is the imaginary part
b = 4 - 5j  # 4 is the real part, and -5j is the imaginary part
print("Real part of a:", a.real)        # Real part of a: 2.0
print("Imaginary part of a:", a.imag)   # Imaginary part of a: 3.0

# 3. String: Used for text
person_name = "John Doe"
# 4. Boolean: Used for true/false values
is_student = True
# 5. None: Used to represent the absence of a value
address = None


# 6. List: Used for ordered collections of items
fruits = ["apple", "banana", "cherry"]
# 7. Dictionary: Used for key-value pairs
student_info = {"name": "Alice", "grade": "A", "age": 22}
# 8.Tuple: Used for ordered, immutable collections of items
coordinates = (40.7128, -74.0060)
# 9. Set: Used for unordered collections of unique items
unique_numbers = {1, 2, 3, 4, 5}




# Print the variables
print("Age:", age)
print("Average Score:", average_score)
print("Name:", person_name)
print("Is Student:", is_student)
print("Address:", address)
print("Fruits:", fruits)
print("Student Info:", student_info)
print("Coordinates:", coordinates)
print("Unique Numbers:", unique_numbers)
